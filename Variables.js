//  variables it is just way to declare a value or initialize value.
// There are four way to declare a variables.

// 1] using var -
var a=10;
console.log(a);

// 2] using let -
let b=20;
console.log(b);

// 3] using const -
// we use const after we can not change the value of variables.
const c=30;
console.log(c);

// 4] using nothing -
d=40;
console.log(d);